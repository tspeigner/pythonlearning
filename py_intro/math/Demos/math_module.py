import math

print("\nmath.ceil()")
print(math.ceil(5.4)) #Returns 6
print(math.ceil(-5.4)) #Returns -5

print("\nmath.floor()")
print(math.floor(5.6)) #Returns 5
print(math.floor(-5.6)) #Returns -6

print("\nmath.trunc()")
print(math.trunc(5.6)) #Returns 5
print(math.trunc(-5.6)) #Returns -5

print("\nmath.fabs()")
print(math.fabs(-5)) #returns 5.0
print(abs(-5)) #returns 5

print("\nmath.factorial()")
print(math.factorial(3)) #returns 6
print(math.factorial(5)) #returns 120

print("\nmath.fmod()")
print(math.fmod(5,2)) #returns 1
print(math.fmod(11,3)) #returns 2
print(math.fmod(22,4)) #returns 2
print(math.fmod(22,3)) #returns 1
print(math.fmod(10934,324)) #returns 242

print("\nmath.pow()")
print(math.pow(5,2)) #returns 25.0

print("\nmath.sqrt()")
print(math.sqrt(25)) #returns 5.0